_PRESET_MAP: dict[str, Persona] = {p.key: p for p in _PRESETS}

# @name / @emoji / @key → key mapping
_NAME_TO_KEY: dict[str, str] = {}
for _p in _PRESETS:
    _NAME_TO_KEY[_p.name]  = _p.key
    _NAME_TO_KEY[_p.emoji] = _p.key
    _NAME_TO_KEY[_p.key]   = _p.key


def build_personas(custom_list: list[dict] | None = None) -> dict[str, Persona]:
    """Merge presets with user-defined custom personas."""
    result = dict(_PRESET_MAP)
    for raw in (custom_list or []):
        try:
            p = Persona(
                key=raw["key"],
                name=raw.get("name", raw["key"]),
                emoji=raw.get("emoji", "👤"),
                color=raw.get("color", "#FFFFFF"),
                triggers=raw.get("triggers", []),
                system=raw.get("system") or _make_custom_system(raw),
            )
            result[p.key] = p
            _NAME_TO_KEY[p.name]  = p.key
            _NAME_TO_KEY[p.emoji] = p.key
            _NAME_TO_KEY[p.key]   = p.key
        except (KeyError, TypeError):
            pass
    return result


def _make_custom_system(raw: dict) -> str:
    style = raw.get("style", "专业，回复简洁")
    name  = raw.get("name", raw.get("key", "吧友"))
    emoji = raw.get("emoji", "👤")
    return (
        f'你正在"ErnieCLI技术吧"里回帖。你的角色是 **{emoji}{name}**。\n'
        f"说话风格：{style}\n"
        "发言规则：用中文，200字以内，直接发表观点。"
        "引用其他楼层时用格式：`>> 引用@名字: 摘要`。"
    )


def resolve_mention(mention: str) -> str | None:
    """Resolve @mention text to a persona key."""
    text = mention.strip().lstrip("@")
    return _NAME_TO_KEY.get(text)
